package AnnaStudio.composicao;


public class Cargos {
	private String NomeCargo;
    private String NivelEmpresa;
	
    public String getNomeCargo() {
		return NomeCargo;
	}
	public void setNomeCargo(String nomeCargo) {
		NomeCargo = nomeCargo;
	}
	public String getNivelEmpresa() {
		return NivelEmpresa;
	}
	public void setNivelEmpresa(String nivelEmpresa) {
		NivelEmpresa = nivelEmpresa;
	}

}
